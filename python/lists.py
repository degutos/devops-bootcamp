# Lists

# normal list
list1 = [1, 2, 3, 4, 5]
print(list1[4])

# Changing a value in the list
numbers = [1, 2, 3, 4, 5]
numbers[4] = 6
print(numbers[4])

list1 = [1, 2, 3, 4, 5]
list1[0] = 10
print(list1)




# Showing Reverse List
list1=[2,5,3,1]
print(list1[::-1])


# Print first and item [3] in the list
list1 = [10, 11, 12, 13, 14]
print(list1[::3])